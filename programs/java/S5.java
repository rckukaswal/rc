public class S5 {
	public static void main(String[] args) {
		for (int i = 100; i >= 60; i--) {

			if (i >= 71) {
				System.out.print(i + ", ");

			} else if (i >= 70) {

				System.out.println(", " + i);

			} else if (i >= 60) {
				System.out.print(i + ", ");
			}

		}
	}

}
