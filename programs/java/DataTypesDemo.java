public class DataTypesDemo {
    public static void main(String[] args) {
        int a = 10;
        double b = 20.5;
        char c = 'A';
        boolean d = true;

        System.out.println("Integer: " + a);
        System.out.println("Double: " + b);
        System.out.println("Char: " + c);
        System.out.println("Boolean: " + d);
    }
}
